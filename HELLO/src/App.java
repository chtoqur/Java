public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Hello, Iseo");
    }
}

// 1. JAVA는 어플리케이션을 class의 집합으로 본다.
//    > 혼자 class 외부에 단독으로 존재하는 함수나 변수 등은 존재하지 않음
//    > 파일이름과 클래스이름은 같게 생성 (강제는 아니나, 구버전 자바는 강제)
// 2. main함수()는 어플리케이션의 시작점 (Entry Point Function)
//    > 문법적으로 약속되어져 있는 함수
//    > 따라서 하나의 프로젝트에는 반드시 한개의 main함수()만 존재해야 한다.
